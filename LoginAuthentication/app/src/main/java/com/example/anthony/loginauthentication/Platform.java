package com.example.anthony.loginauthentication;

public class Platform {

    private String platformName; //e.g. work, college
    private String platformPass;

    /*****SETTERS & GETTERS*****/
    public String getPlatformName() {
        return platformName;
    }

    public void setPlatformName(String platformName) {
        this.platformName = platformName;
    }

    public String getPlatformPass() {
        return platformPass;
    }

    public void setPlatformPass(String platformPass) {
        this.platformPass = platformPass;
    }
    /*****END SETTERS & GETTERS*****/
}
